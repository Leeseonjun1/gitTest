package pack03_Decimal;

public class Ex01_Decimal {

	public static void main(String[] args) {
		
		int i = 1234567890;
		System.out.println(i);
		// int 이상의 값을 담을 수 있는 정수형 데이터타입이 존재함.
		// long형 l1이라는 변수를 선언하기
		// long형 l2 라는 변수를 초기화 시기키.123456790
		 long l1;
		 long l2 = 1234567901L ;// 숫자+long타입
		 System.out.println(l2);
		 
		 //실수형 데이터 타입: float , double
		 //실수형은 소숫점이 있는것을 표현하기 위해 존재
		 //float는 long과 마찬가지로 구분자 F 붙여줘야함
           
		 //float를 사용하여 변수선언 ,할당,초기화
		 //double 을이용하여 변수선언
		 
		 float c;
		 c=3.3F;
		 
		 float a =23.5F;
		 double b = 6.1;
		 
		 System.out.println(a);
		 System.out.println(b);
		 System.out.println(c);
		 // 뒤에 구분자가 붙는 long , float 
		 
		
		
		
		
	}
	
}
